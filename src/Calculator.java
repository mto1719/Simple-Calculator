public class Calculator
{
	public int add(int num1, int num2) 
	{
		int ans = num1 + num2;
		return ans;
	}	
	int sub(int num1, int num2) 
	{
		int ans = num1 - num2;
		return ans;	
	}
	int mul(int num1, int num2)
	{
		int ans = num1 * num2;
		return ans;
	}
	int div(int num1, int num2)
	{
		int ans = num1 / num2;
		return ans;
	}
}
